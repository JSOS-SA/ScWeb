/**
 * يُرجع كل القوائم بشكل صفوف جاهزة لإدراجها في جدول lists
 * كل سجل: { listType, value, order }
 */
export function buildSeedRows() {
  const rows = [];
  for (const [listType, values] of Object.entries(SEED_LISTS)) {
    values.forEach((value, index) => {
      rows.push({
        listType,
        value,
        order: index,
        active: true
      });
    });
  }
  return rows;
}
